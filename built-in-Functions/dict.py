
# * สร้างพจนานุกรม (dictionary) numbers โดยกำหนดคู่คีย์-ค่า x=5 และ y=0
numbers = dict(x=5, y=0)

# * แสดงค่าของ numbers และประเภทของข้อมูลของ numbers
print('numbers =', numbers)
print(type(numbers))

# * สร้างพจนานุกรม (dictionary) ว่าง (empty)
empty = dict()

# * แสดงค่าของ empty และประเภทของข้อมูลของ empty
print('empty =', empty)
print(type(empty))
