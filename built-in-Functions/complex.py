
# * สร้างตัวแปร z และกำหนดค่าให้เป็นจำนวนเชิงซ้อน complex(2, -3)
z = complex(2, -3)
print(z)

# * สร้างตัวแปร z และกำหนดค่าให้เป็นจำนวนเชิงซ้อน complex(1, 0) โดย default ค่าส่วนจริงเป็น 0
z = complex(1)
print(z)

# * สร้างตัวแปร z และกำหนดค่าให้เป็นจำนวนเชิงซ้อน complex(0, 0) โดย default ค่าส่วนจริงและส่วนหน่วยเป็น 0
z = complex()
print(z)

# * สร้างตัวแปร z และกำหนดค่าให้เป็นจำนวนเชิงซ้อน complex(5, -9) โดยรับค่าจากสตริง '5-9j'
z = complex('5-9j')
print(z)
